'use client';

import { motion, useScroll, useSpring } from 'framer-motion';

export default function ScrollProgress() {
  const { scrollYProgress } = useScroll();
  const scaleX = useSpring(scrollYProgress, {
    stiffness: 100,
    damping: 30,
    restDelta: 0.001
  });

  return (
    <motion.div
      className="fixed top-0 left-0 right-0 h-1 sm:h-1.5 bg-gradient-to-r from-honey via-green to-pink origin-left z-50 shadow-lg"
      style={{ scaleX }}
    />
  );
}
